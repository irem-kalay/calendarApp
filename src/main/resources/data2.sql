SELECT 
    h.calendar_date,
    h.is_full_day,
    hd.name AS holiday_name,
    h.notes,
    ht.name AS holiday_type
FROM 
    holidays h,
    holiday_periods hp,
    holiday_definitions hd,
    holiday_types ht
WHERE 
    h.period_id = hp.id
    AND hp.definition_id = hd.id
    AND hd.type_id = ht.id
ORDER BY 
    h.calendar_date ASC;

---b√∂lge ve ki≈üiye g√∂re tatiller
SELECT 
    h.calendar_date,
    hd.name AS holiday_name,
    ht.name AS holiday_type,
    CASE 
        WHEN h.is_full_day = 1 THEN 'Tam G√ºn'
        WHEN h.is_full_day = 2 THEN 'Yarƒ±m G√ºn'
    END AS day_type,
    r.countryname,
    pt.name AS person_type
FROM 
    holidays h,
    holiday_periods hp,
    holiday_definitions hd,
    holiday_types ht,
    junction j,
    region r,
    persontypes pt
WHERE 
    h.period_id = hp.id
    AND hp.definition_id = hd.id
    AND hd.type_id = ht.id
    AND h.id = j.holiday_id
    AND j.region_id = r.id
    AND j.person_type_id = pt.id
    AND r.countrycode = 'TR'  -- Sadece T√ºrkiye tatilleri i√ßin
ORDER BY 
    h.calendar_date ASC, 
    pt.name ASC;

--tatil periyotlarƒ±
SELECT 
    hp.start_date,
    hp.end_date,
    hd.name AS holiday_name,
    ht.name AS holiday_type,
    (hp.end_date - hp.start_date + 1) AS total_days
FROM 
    holiday_periods hp,
    holiday_definitions hd,
    holiday_types ht
WHERE 
    hp.definition_id = hd.id
    AND hd.type_id = ht.id
ORDER BY 
    hp.start_date ASC;

---2025 yƒ±lƒ± tatil takvimi
SELECT 
    TO_CHAR(h.calendar_date, 'DD-MM-YYYY Day', 'NLS_DATE_LANGUAGE=Turkish') AS tarih,
    hd.name AS tatil_adi,
    ht.name AS tatil_turu,
    CASE 
        WHEN h.is_full_day = 1 THEN 'Tam G√ºn Tatil'
        WHEN h.is_full_day = 2 THEN 'Yarƒ±m G√ºn Tatil (Arife)'
    END AS gun_tipi
FROM 
    holidays h,
    holiday_periods hp,
    holiday_definitions hd,
    holiday_types ht
WHERE 
    h.period_id = hp.id
    AND hp.definition_id = hd.id
    AND hd.type_id = ht.id
    AND EXTRACT(YEAR FROM h.calendar_date) = 2025
ORDER BY 
    h.calendar_date ASC;


