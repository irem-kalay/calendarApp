    start_date     DATE NOT NULL,
    end_date       DATE NOT NULL,
    CONSTRAINT fk_hp_definition FOREIGN KEY (definition_id) REFERENCES holiday_definitions (id)
);


-- --- A≈üama 3: Tatil G√ºnleri (API Performans Katmanƒ± - T√ºretilmi≈ü) ---
