    END AS gun_tipi,
    h.notes AS aciklama
FROM 
    holidays h,
    holiday_periods hp,
    holiday_definitions hd,
    holiday_types ht,
    junction j,
    persontypes pt
WHERE 
    h.period_id = hp.id
    AND hp.definition_id = hd.id
    AND hd.type_id = ht.id
    AND h.id = j.holiday_id
    AND j.person_type_id = pt.id
